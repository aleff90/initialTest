package operatorIncreaseDecrease;

public class Main {
    public static void main(String[] args) {
        int numero = 5;
        numero++;
        System.out.println("\n Variavel numero com valor 5 incrementa mais 1: "+numero);
        numero--;
        System.out.println("\n Variavel numero com valor 6 decrementa menos 1: "+numero);
    }
}
