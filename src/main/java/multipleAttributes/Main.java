package multipleAttributes;

public class Main {
    String fname = "Svetlana";
    String lname = "Pavlov";
    int age = 45;

    public static void main(String[] args) {
        Main myObj = new Main();
        System.out.println("\n Name: "+myObj.fname+" "+myObj.lname);
        System.out.println("\n Age: "+myObj.age);
    }
}
